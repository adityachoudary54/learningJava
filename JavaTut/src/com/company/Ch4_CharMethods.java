package com.company;

public class Ch4_CharMethods {
    public static void main(String[] args) {
        System.out.println("Character Methods");
        System.out.println("isDigit(2):"+Character.isDigit('2'));
        System.out.println("isLetter(2):"+Character.isLetter('2'));
        System.out.println("isLetterOrDigit(2):"+Character.isLetterOrDigit('2'));
        System.out.println("isLowerCase(a):"+Character.isLowerCase('a'));
        System.out.println("isUpperCase(a):"+Character.isUpperCase('a'));
        System.out.println("toLowerCase(a):"+Character.toLowerCase('A'));
        System.out.println("toUpperCase(a):"+Character.toUpperCase('a'));
    }
}
